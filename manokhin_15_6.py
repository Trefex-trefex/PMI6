def isprime(x):
    flag = x > 1
    d = 2
    while flag and d < int(x**0.5) + 1:
        flag = x % d != 0
        d += 1
    return flag


def f(a):
    a.sort()
    ans = {"primes": [], "composites": []} 
    for i in a:
        if isprime(i):
            ans["primes"].append(i)
        else:
            ans["composites"].append(i)
    return ans


