def f(s):
    '''
    функция ищет количество подстрок "101"
    '''
    cnt = 0
    i = 0
    while i < len(s)-2:
        if s[i]+s[i+1]+s[i+2] == '101':
            cnt += 1
            i += 1
        i += 1
    return cnt

#пример
a = '1101 1010101 1011001'

print(f(a))
        
            
        
